    private void cargarMaterias() {
        List<Materia> materias = materiaData.listarMaterias();
        for (Materia item : materias) {
            cbMaterias.addItem(item);
        }
    }

    public void armarCabeceraTabla() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("ID");
        filaCabecera.add("DNI");
        filaCabecera.add("Apellido");
        filaCabecera.add("Nombre");
        for (Object it : filaCabecera) {
            model.addColumn(it);
        }
        tAlumnos.setModel(model);

    }

    public void borrarFilasTabla() {
        int a = model.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            model.removeRow(i);

        }
    }

    public void cargarDatos() {

        this.borrarFilasTabla();
        Materia materiaSeleccionada = (Materia) cbMaterias.getSelectedItem();
        List<Alumno> alumnos = inscripcionData.obtenerAlumnosPorMateria(materiaSeleccionada.getIdMateria());
        for (Alumno alum : alumnos) {
            model.addRow(new Object[]{alum.getIdAlumno(), alum.getDNI(), alum.getApellido(), alum.getNombre()});
        }

    }

